				<p>
					IEEE eLearning Library is a collection of online educational courses designed for self-paced learning.
					Education Partners, exclusive for IEEE members, offers on-line degree programs, certifications and
					courses at a 10% discount. The Standards in Education website explains what standards are and the
					importance of developing and using them. The site includes tutorial modules and case illustrations to
					introduce the history of standards, the basic terminology, their applications and impact on products, as
					well as news related to standards, book reviews and links to other sites that contain information on
					standards. Currently, twenty-nine states in the United States require Professional Development Hours
					(PDH) to maintain a Professional Engineering license,[27][28] encouraging engineers to seek Continuing
					Education Units (CEUs) for their participation in continuing education programs. CEUs readily translate
					into Professional Development Hours (PDHs), with 1 CEU being equivalent to 10 PDHs. Countries outside
					the United States, such as South Africa, similarly require continuing professional development (CPD)
					credits, and it is anticipated that IEEE Expert Now courses will feature in the CPD listing for South
					Africa.
